#include <stdlib.h>
#include <unistd.h>
#include <stddef.h>
#include "libft.h"

int	ft_toupper(int c)
{
	if (c >= 'a' && c <= 'z')
		return (c - 32);
	else if (c >= 128)
		return (c);
	return (c);
}
